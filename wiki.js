
function openNav() {
  document.getElementById("mySidenav").style.width = "250px";
  document.getElementById("wiki-container").style.marginLeft = "280px";
  document.getElementById("wiki-container").style.width="75%"
}

function closeNav() {
  document.getElementById("mySidenav").style.width = "0";
  document.getElementById("wiki-container").style.marginLeft = "100px";
  document.getElementById("wiki-container").style.width="90%"
}